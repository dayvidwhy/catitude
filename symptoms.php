					<p>
						Cats and in particular kittens will frequently scratch their human companions without intending any harm particularly when playing or if given a fright. If panicked, a cat will often claw their way to freedom even if this causes unintentional damage to the person who is holding them or on whom they are sitting. 
					</p>
